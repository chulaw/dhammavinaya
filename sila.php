      <h3>Vācāyacariyā</h3><br/>
      “Kathañca, gahapatayo, catubbidhaṃ vācāya adhamma­cari­yā­vi­sama­cariyā hoti? Idha, gahapatayo, ekacco musāvādī hoti. Sabhāgato vā parisāgato vā, ñātimajjhagato vā pūgamajjhagato vā rāja­kula­maj­jha­gato vā, abhinīto sakkhipuṭṭho: ‘ehambho purisa, yaṃ jānāsi taṃ vadehī’ti, so ajānaṃ vā āha: ‘jānāmī’ti, jānaṃ vā āha: ‘na jānāmī’ti, apassaṃ vā āha: ‘passāmī’ti, passaṃ vā āha: ‘na passāmī’ti. Iti attahetu vā parahetu vā āmisa­kiñcik­kha­hetu vā sampajānamusā bhāsitā hoti.<br/><br/><br/>
      Pisuṇavāco kho pana hoti. Ito sutvā amutra akkhātā imesaṃ bhedāya, amutra vā sutvā imesaṃ akkhātā amūsaṃ bhedāya. Iti samaggānaṃ vā bhettā, bhinnānaṃ vā anuppadātā, vaggārāmo vaggarato vagganandī vaggakaraṇiṃ vācaṃ bhāsitā hoti.<br/><br/><br/><br/>
      Pharusavāco kho pana hoti. Yā sā vācā aṇḍakā kakkasā parakaṭukā parābhisajjanī kodhasāmantā asamā­dhi­saṃ­vatta­nikā, tathārūpiṃ vācaṃ bhāsitā hoti.<br/><br/>
      Samphappalāpī kho pana hoti. Akālavādī abhūtavādī anatthavādī adhammavādī avinayavādī. Anidhānavatiṃ vācaṃ bhāsitā hoti akālena anapadesaṃ apariyan­ta­vatiṃ anat­tha­saṃhi­taṃ. Evaṃ kho, gahapatayo, catubbidhaṃ vācāya adhamma­cari­yā­vi­sama­cariyā hoti.<br/><br/>
      Kathañca, gahapatayo, catubbidhaṃ vācāya dhamma­cari­yā­sama­cariyā hoti? Idha, gahapatayo, ekacco musāvādaṃ pahāya musāvādā paṭivirato hoti. Sabhāgato vā parisāgato vā, ñātimajjhagato vā pūgamajjhagato vā rāja­kula­maj­jha­gato vā, abhinīto sakkhipuṭṭho: ‘ehambho purisa, yaṃ jānāsi taṃ vadehī’ti, so ajānaṃ vā āha: ‘na jānāmī’ti, jānaṃ vā āha: ‘jānāmī’ti, apassaṃ vā āha: ‘na passāmī’ti, passaṃ vā āha: ‘passāmī’ti. Iti attahetu vā parahetu vā āmisa­kiñcik­kha­hetu vā na sampajānamusā bhāsitā hoti.<br/><br/><br/><br/>
      Pisuṇaṃ vācaṃ pahāya pisuṇāya vācāya paṭivirato hoti, ito sutvā na amutra akkhātā imesaṃ bhedāya, amutra vā sutvā na imesaṃ akkhātā amūsaṃ bhedāya. Iti bhinnānaṃ vā sandhātā, sahitānaṃ vā anuppadātā, samaggārāmo samaggarato samagganandī samaggakaraṇiṃ vācaṃ bhāsitā hoti.<br/><br/><br/>
      Pharusaṃ vācaṃ pahāya pharusāya vācāya paṭivirato hoti. Yā sā vācā nelā kaṇṇasukhā pemanīyā hadayaṅgamā porī bahujanakantā bahujanamanāpā—tathārūpiṃ vācaṃ bhāsitā hoti.<br/><br/>
      Samphappalāpaṃ pahāya samphappalāpā paṭivirato hoti. Kālavādī bhūtavādī atthavādī dhammavādī vinayavādī nidhānavatiṃ vācaṃ bhāsitā hoti kālena sāpadesaṃ pariyantavatiṃ atthasaṃhitaṃ. Evaṃ kho, gahapatayo, catubbidhaṃ vācāya dhamma­cari­yā­sama­cariyā hoti.”
