      <h3>Verbal Conduct</h3><br/>
      “And how are there four sorts of un-Dhamma conduct, dissonant conduct with speech? There is the case where a certain person engages in false speech. When he has been called to a town meeting, a group meeting, a gathering of his relatives, his guild, or of the royalty, if he is asked as a witness, ‘Come and tell, good man, what you know’: If he doesn’t know, he says, ‘I know.’ If he does know, he says, ‘I don’t know.’ If he hasn’t seen, he says, ‘I have seen.’ If he has seen, he says, ‘I haven’t seen.’ Thus he consciously tells lies for his own sake, for the sake of another, or for the sake of a certain reward.<br/><br/>
      He engages in divisive speech. What he has heard here he tells there to break those people apart from these people here. What he has heard there he tells here to break these people apart from those people there. Thus breaking apart those who are united and stirring up strife between those who have broken apart, he loves factionalism, delights in factionalism, enjoys factionalism, speaks things that create factionalism.<br/><br/>
      He engages in abusive speech. He speaks words that are harsh, cutting, bitter to others, abusive of others, provoking anger and destroying concentration.<br/><br/>
      He engages in idle chatter. He speaks out of season, speaks what isn’t factual, what isn’t in accordance with the goal, the Dhamma and the Vinaya, words that are not worth treasuring. This is how there are four sorts of un-Dhamma conduct, dissonant conduct with speech.<br/><br/>
      And how are there three sorts of Dhamma conduct, harmonious conduct with speech? There is the case where a certain person, abandoning false speech, abstains from false speech. When he has been called to a town meeting, a group meeting, a gathering of his relatives, his guild, or of the royalty, if he is asked as a witness, ‘Come and tell, good man, what you know’: If he doesn’t know, he says, ‘I don’t know.’ If he does know, he says, ‘I know.’ If he hasn’t seen, he says, ‘I haven’t seen.’ If he has seen, he says, ‘I have seen.’ Thus he doesn’t consciously tell a lie for his own sake, for the sake of another, or for the sake of any reward.<br/><br/>
      Abandoning divisive speech he abstains from divisive speech. What he has heard here he doesn’t tell there to break those people apart from these people here. What he has heard there he doesn’t tell here to break these people apart from those people there. Thus reconciling those who have broken apart or cementing those who are united, he loves concord, delights in concord, enjoys concord, speaks things that create concord.<br/><br/>
      Abandoning abusive speech, he abstains from abusive speech. He speaks words that are soothing to the ear, that are affectionate, that go to the heart, that are polite, appealing and pleasing to people at large.<br/><br/>
      Abandoning idle chatter, he abstains from idle chatter. He speaks in season, speaks what is factual, what is in accordance with the goal, the Dhamma and the Vinaya. He speaks words worth treasuring, seasonable, reasonable, circumscribed, connected with the goal. This is how there are four sorts of Dhamma conduct, harmonious conduct with speech.”
