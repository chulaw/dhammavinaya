      <h3>Kāyenacariyā</h3><br/>
      “Kathañca, gahapatayo, tividhaṃ kāyena adhamma­cari­yā­vi­sama­cariyā hoti? Idha, gahapatayo, ekacco pāṇātipātī hoti, luddo lohitapāṇi hatappahate niviṭṭho adayāpanno pāṇabhūtesu.<br/><br/>
      Adinnādāyī kho pana hoti. Yaṃ taṃ parassa para­vit­tūpaka­ra­ṇaṃ, gāmagataṃ vā araññagataṃ vā, taṃ adinnaṃ they­ya­saṅ­khā­taṃ ādātā hoti.<br/><br/>
      Kāmesu­micchā­cārī kho pana hoti. Yā tā māturakkhitā piturakkhitā ­mātāpi­tu­rak­khitā bhāturakkhitā bhagi­ni­rak­khitā ñātirakkhitā gottarakkhitā dhammarakkhitā sassāmikā saparidaṇḍā antamaso mālā­guḷa­parik­khit­tāpi, tathārūpāsu cārittaṃ āpajjitā hoti. Evaṃ kho, gahapatayo, tividhaṃ kāyena adhamma­cari­yā­vi­sama­cariyā hoti.<br/><br/><br/>
      Kathañca, gahapatayo, tividhaṃ kāyena dhamma­cari­yā­sama­cariyā hoti? Idha, gahapatayo, ekacco pāṇātipātaṃ pahāya pāṇātipātā paṭivirato hoti, nihitadaṇḍo nihitasattho lajjī dayāpanno sabba­pāṇa­bhūta­hi­tānukampī viharati.<br/><br/><br/>
      Adinnādānaṃ pahāya adinnādānā paṭivirato hoti. Yaṃ taṃ parassa para­vit­tūpaka­ra­ṇaṃ, gāmagataṃ vā araññagataṃ vā, taṃ nādinnaṃ they­ya­saṅ­khā­taṃ ādātā hoti.<br/><br/><br/>
      Kāmesu­micchā­cāraṃ pahāya kāmesu­micchā­cārā paṭivirato hoti. Yā tā māturakkhitā piturakkhitā ­mātāpi­tu­rak­khitā bhāturakkhitā bhagi­ni­rak­khitā ñātirakkhitā gottarakkhitā dhammarakkhitā sassāmikā saparidaṇḍā antamaso mālā­guḷa­parik­khit­tāpi, tathārūpāsu na cārittaṃ āpajjitā hoti. Evaṃ kho, gahapatayo, tividhaṃ kāyena dhamma­cari­yā­sama­cariyā hoti.”
