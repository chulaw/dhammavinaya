      <h3>Bodily Conduct</h3><br/>
      “And how are there three sorts of un-Dhamma conduct, dissonant conduct with the body? There is the case where a certain person takes life, is brutal, bloody-handed, devoted to killing and slaying, showing no mercy to living beings.<br/><br/>
      He takes what is not given. He takes, in the manner of a thief, things in a village or a wilderness that belong to others and have not been given by them.<br/><br/>
      He engages in sensual misconduct. He gets sexually involved with those who are protected by their mothers, their fathers, their brothers, their sisters, their relatives, or their Dhamma; those with husbands, those who entail punishments, or even those crowned with flowers by another man. This is how there are three sorts of un-Dhamma conduct, dissonant conduct with the body.<br/><br/>
      And how are there three sorts of Dhamma conduct, harmonious conduct with the body? There is the case where a certain person, abandoning the taking of life, abstains from the taking of life. He dwells with his rod laid down, his knife laid down, scrupulous, merciful, compassionate for the welfare of all living beings.<br/><br/>
      Abandoning the taking of what is not given, he abstains from taking what is not given. He doesn’t take, in the manner of a thief, things in a village or a wilderness that belong to others and have not been given by them.<br/><br/>
      Abandoning sensual misconduct, he abstains from sensual misconduct. He doesn’t get sexually involved with those who are protected by their mothers, their fathers, their brothers, their sisters, their relatives, or their Dhamma; those with husbands, those who entail punishments, or even those crowned with flowers by another man. This is how there are three sorts of Dhamma conduct, harmonious conduct with the body.”
