      <h3>Tatra kālaññū vācāya</h3><br/>
      “Evameva kho, rājakumāra, yaṃ tathāgato vācaṃ jānāti abhūtaṃ atacchaṃ anat­tha­saṃhi­taṃ sā ca paresaṃ appiyā amanāpā, na taṃ tathāgato vācaṃ bhāsati.<br/><br/>
       Yampi tathāgato vācaṃ jānāti bhūtaṃ tacchaṃ anat­tha­saṃhi­taṃ sā ca paresaṃ appiyā amanāpā, tampi tathāgato vācaṃ na bhāsati.<br/><br/>
       Yañca kho tathāgato vācaṃ jānāti bhūtaṃ tacchaṃ atthasaṃhitaṃ sā ca paresaṃ appiyā amanāpā, tatra kālaññū tathāgato hoti tassā vācāya veyyākaraṇāya.<br/><br/><br/>
       Yaṃ tathāgato vācaṃ jānāti abhūtaṃ atacchaṃ anat­tha­saṃhi­taṃ sā ca paresaṃ piyā manāpā, na taṃ tathāgato vācaṃ bhāsati.<br/><br/>
       Yampi tathāgato vācaṃ jānāti bhūtaṃ tacchaṃ anat­tha­saṃhi­taṃ sā ca paresaṃ piyā manāpā tampi tathāgato vācaṃ na bhāsati.<br/><br/>
       Yañca tathāgato vācaṃ jānāti bhūtaṃ tacchaṃ atthasaṃhitaṃ sā ca paresaṃ piyā manāpā, tatra kālaññū tathāgato hoti tassā vācāya veyyākaraṇāya.<br/><br/>
       Taṃ kissa hetu? Atthi, rājakumāra, tathāgatassa sattesu anukampā”ti.
