      <h3>Proper Time for Speech</h3><br/>
      “In the same way, prince: in the case of words that the Tathāgata knows to be unfactual, untrue, unbeneficial, unendearing and disagreeable to others, he does not say them.<br/><br/>
      In the case of words that the Tathāgata knows to be factual, true, unbeneficial, unendearing and disagreeable to others, he does not say them.<br/><br/>
      In the case of words that the Tathāgata knows to be factual, true, beneficial, but unendearing and disagreeable to others, he has a sense of the proper time for saying them.<br/><br/>
      In the case of words that the Tathāgata knows to be unfactual, untrue, unbeneficial, but endearing and agreeable to others, he does not say them.<br/><br/>
      In the case of words that the Tathāgata knows to be factual, true, unbeneficial, but endearing and agreeable to others, he does not say them.<br/><br/>
      In the case of words that the Tathāgata knows to be factual, true, beneficial, and endearing and agreeable to others, he has a sense of the proper time for saying them.<br/><br/>
      Why is that? Because the Tathāgata has sympathy for living beings.”
